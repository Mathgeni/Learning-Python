def get_word():
    return choice(universum)


def display_hangman(tries):
    stages = [  '''
                   --------
                   |      |
                   |      O
                   |     \\|/
                   |      |
                   |     / \\
                   -
                ''',
                # голова, торс, обе руки, одна нога
                '''
                   --------
                   |      |
                   |      O
                   |     \\|/
                   |      |
                   |     / 
                   -
                ''',
                # голова, торс, обе руки
                '''
                   --------
                   |      |
                   |      O
                   |     \\|/
                   |      |
                   |      
                   -
                ''',
                # голова, торс и одна рука
                '''
                   --------
                   |      |
                   |      O
                   |     \\|
                   |      |
                   |     
                   -
                ''',
                # голова и торс
                '''
                   --------
                   |      |
                   |      O
                   |      |
                   |      |
                   |     
                   -
                ''',
                # голова
                '''
                   --------
                   |      |
                   |      O
                   |    
                   |      
                   |     
                   -
                ''',
                # начальное состояние
                '''
                   --------
                   |      |
                   |      
                   |    
                   |      
                   |     
                   -
                '''
    ]
    return stages[tries]


def play(word):
    word1 = word
    list_out = [i for i in '_' * len(word)]
    flag = False
    guessed_letters = []
    tries = 6
    while tries != 0:
        print(display_hangman(tries))
        print('Введите букву:')
        letter = input()
        if letter in word:
            while letter in word:
                list_out[word.find(letter)] = letter
                word = word.replace(letter, '0', 1)
            print(*list_out)
        else:
            tries -= 1
            print(*list_out)
    if word.isdigit():
        print("Победа!")
    print(display_hangman(0))
    print(word1)


play(get_word())
